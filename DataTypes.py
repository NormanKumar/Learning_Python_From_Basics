# There are 5 types of Data Types
name = "Norman"
age = 67
is_male = True
weight = 103.5
print(name)
print(age)
print(is_male)
print(weight)

# To know the type of the variables With Variables
print(type(name))
print(type(age))
print(type(is_male))
print(type(weight))

# Without using variables
print(type(90))
print(type("Norman"))
print(type(True))
print(type(90.8))